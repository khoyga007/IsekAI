const TABLES: Record<Lang, Dict> = { en: EN, vi: VI };

export function translate(lang: Lang, key: string, vars?: Record<string, string>): string {
  const t = TABLES[lang]?.[key] ?? TABLES.en[key] ?? key;
  if (!vars) return t;
  return t.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? `{${k}}`);
}

/** Hook — reactively pulls language from settings store. */
export function useT() {
  const lang = useSettings((s) => s.ui.lang);
  return (key: string, vars?: Record<string, string>) => translate(lang, key, vars);
}

/** Non-hook accessor for use inside event handlers / non-React code. */
export function tNow(key: string, vars?: Record<string, string>): string {
  const lang = useSettings.getState().ui.lang;
  return translate(lang, key, vars);
}
